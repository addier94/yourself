<template>
  <div>
    <Header
      title="Manage your Blogs"
      exitLink="/"
    />
    <div class="instructor-blogs">
      <div class="container">
        <div class="section">
          <div class="header-block">
            <h2>Historial de publicaciones</h2>
            <div class="title-menu">
              <button @click="$router.push('/instructor/blog/editor')" class="button">Crear una publicación</button>
            </div>
          </div>
          <div class="tabs">
            <ul>
              <!-- set here active tab -->
              <li @click="activeTab = 0">
                <a :class="{'is-active': activeTab === 0}">Articulos en borrador</a>
                </li>
              <!-- set here active tab -->
              <li @click="activeTab = 1">
                <a :class="{'is-active': activeTab === 1}">Publicados</a>
                </li>
            </ul>
          </div>
          <div class="blogs-container">
            <!-- Draft Blogs -->
            <!-- Check-for-active-tab -->
            <template v-if="activeTab === 0">
              <div v-if="drafts && drafts.length > 0">
                <div  
                      v-for="dBlog in drafts" 
                      :key="dBlog._id"
                      class="blog-card">
                  <h2>{{ dBlog.title }}</h2>
                  <div class="blog-card-footer">
                    <span>
                      Ultima edición {{ dBlog.updatedAt | formatDate }}
                    </span>
                    <dropdown :items="draftsOptions" />
                  </div>
                </div>
              </div>
              <!-- In case of no drafts blogs  -->
              <div v-else class="blog-error">
                Aun no hay blogs :(
              </div>
            </template>
            <!-- Published Blogs -->
            <!-- check for active tab -->
            <template v-if="activeTab === 1">
              <div v-if="published && published.length > 0">
                <!-- make iteration here for published -->
                <div
                      v-for="pBlog in published"
                      :key="pBlog._id"
                      class="blog-card">
                  <!-- title -->
                  <h2>{{ pBlog.title }}</h2>
                  <div class="blog-card-footer">
                    <!-- updated -->
                    <span>
                      Ultima Edición {{ pBlog.updatedAt | formatDate }}
                    </span>
                    <dropdown :items="publishedOptions"/>
                  </div>
                </div>
              </div>
              <!-- In case of no drafts blogs   -->
              <div v-else class="blog-error">
                Aun no hay blogs publicado :(
              </div>
            </template>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>
<script>
import Header from '~/components/shared/Header'
import Dropdown from '~/components/shared/DropDown'
import { mapState } from 'vuex'
import { createPublishedOptions, createDraftsOptions } from '~/pages/instructor/options'
export default {
  // data with active tab by default it will be 0
  // 0 represents drafts
  // 1 represents published
  layout: 'instructor',
  components: {Header, Dropdown},
  data() {
    return {
      activeTab: 0
    }
  },
  computed: {
    ...mapState({
      published: ({instructor}) => instructor.blog.items.published,
      drafts: ({instructor}) => instructor.blog.items.drafts
    }),
    publishedOptions() {
      return createPublishedOptions()
    },
    draftsOptions() {
      let x = createDraftsOptions()
      return createDraftsOptions()
    }
  },
  async fetch({store}) {
    await store.dispatch('instructor/blog/fetchUserBlogs')
  }
}
</script>

